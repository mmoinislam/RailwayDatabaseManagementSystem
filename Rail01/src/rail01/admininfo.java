    private void btnLoaddataAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoaddataAdminActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) showalldatatable.getModel();
        tableModel.setRowCount(0);

        try {
            Connection myConnect= DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","system","1234");
            Statement myStatement = myConnect.createStatement();
            ResultSet myRS = myStatement.executeQuery("select * from admininfo");
            while(myRS.next()){
                String useridadmin = myRS.getString("user_id");
                String adminid = myRS.getString("admin_id");
                String pass = myRS.getString("pass");
                
                tableModel.addRow(new Object[]{useridadmin,adminid,pass});
            }

            myStatement.close();
        }

        catch (SQLException ex) {
            Logger.getLogger(admininfo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoaddataAdminActionPerformed

