            Connection myConnect= DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","system","1234");
            PreparedStatement myRS = myConnect.prepareStatement("insert into admininfo(user_id,admin_id,pass) values('"+txtUser_idAI.getText()+"','"+txtadmin_idAI.getText()+"','"+txtPass_AI.getText()+"')");
